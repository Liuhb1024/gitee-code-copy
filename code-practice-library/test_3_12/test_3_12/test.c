#define _CRT_SECURE_NO_WARNINGS 1

#include<stdio.h>

//整数的二进制表述方式
//原码
//反码
//补码
int main()
{
	int a = 20;
	//0(符号位)
	//0 000000000000000000000000000 10100 - 原码
	//0 000000000000000000000000000 10100 - 反码
	//0 000000000000000000000000000 10100 - 补码
	int b = -10;
	//1（符号位）
	//1 0000000000000000000000000000 1010 - 原码
	//1 1111111111111111111111111111 0101 - 反码 
	//1 1111111111111111111111111111 0110 - 补码 
	//内存中存储的都是二进制数据
	//
	return 0;
}